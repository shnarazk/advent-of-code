public func day(_ data: String) {
  let lines = Array(data.split(separator: "\n", omittingEmptySubsequences: true))
  part1(lines)
  part2(lines)
}

func part1(_ lines: [String.SubSequence]) {
  var lines = lines
  print("Part1: \(1)")
}

func part2(_ lines: [String.SubSequence]) {
  var lines = lines
  print("Part2: \(2)")
}
